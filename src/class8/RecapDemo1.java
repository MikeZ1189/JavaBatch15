package class8;

public class RecapDemo1 {
    public static void main(String[] args) {
        int i = 10;
        while (i >= 2) {
            System.out.println(i);
            i -= 2;
        }
        System.out.println("----------------------");

        int j = 10;
        do {
            System.out.println(j);
            j -= 2;
        } while (j >= 2);

        System.out.println("----------------------");

        for (int k = 10; k>=2 ; k-=2) {
            System.out.println(k);
        }
            }
        }


