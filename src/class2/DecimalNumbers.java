package class2;

public class DecimalNumbers {
    public static void main(String[] args) {
        /*
        if we want to store decimal numbers (10.5 110.2) using Java we can use the following types
        float
        decimal
         */
        float myNumberBox=10.5f;
        float mySecondBox=5.5f;
        double largeDecimalBox=134545455.12121;
        System.out.println(myNumberBox);
        /*
        if we want to store characters(#, $, o) we can use char datatype
         */
        char letter='a';
        char gender='m';
        char symbol='#';
        char numberChar='1';
        /*
        string date type belong to a different category called non-primitives, this is just the language rules
         */
        String name="Sam";

        boolean hungry=true;
        boolean sleepy=false;
    }
}
