package class7;

public class ForLoops {
    public static void main(String[] args) {

        int number = 0;
        while (number < 10) {
            System.out.println(number);
            number++;
        }
        for(int i=0;i<10;i++){
            System.out.println(i);
}
        for(int h=1;h<20;h+=2){
            System.out.println(h);
        }
    }
}
