package class7;

public class ForLoops1 {
    public static void main(String[] args) {


        for (int h = 0; h < 20; h++) {
            if (h % 2 != 0) {
                System.out.println(h);
            }
        }
        System.out.println("*********");
        for (int i = 1; i < 20; i+=2) {
            System.out.println(i);
        }
    }
}

